"""
python = linguagem de programação
tipo de tipagem = dinâmica / forte
str -> string -> texto
strings são textos que estão dentro de aspas
"""

print(1234)

# aspas simples
print('Luiz Otávio')
print(1, 'Luiz "Otávio"')

# aspas duplas
print("Luiz Otávio")
print(2, "Luiz 'Otávio'")

# escape
print("Luiz \"Otávio\"")

# r
print(r'Luiz \'Otávio\'')